//arrの中にmatchと一致している部分はあるかチェック
//#は#,.は.と一致していればok ?はどちらでもok

const match = [
  ["###.?????"],
  ["###.?????"],
  ["###.?????"],
  ["....?????"],
  ["?????????"],
  ["?????...."],
  ["?????.###"],
  ["?????.###"],
  ["?????.###"],
];

const arr = [
  ["###......###......"],
  ["###......###......"],
  ["###..#...###..#..."],
  ["..............#..."],
  [".................."],
  [".................."],
  ["......###......###"],
  ["......###......###"],
  ["......###......###"],
  [".###.............."],
  [".###......##......"],
  [".###.............."],
  ["............###..."],
  ["...##.......###..."],
  ["...##.......###..."],
  [".......###........"],
  [".......###........"],
  [".......###........"],
  ["........#........."],
];
let ok = [];
for (let i = 0; i < arr.length - match.length + 1; i++) {
  for (let j = 0; j < arr[0][0].length - match[0][0].length + 1; j++) {
    let c = true;
    outermatch: for (let ii = 0; ii < match.length; ii++) {
      for (let jj = 0; jj < match[0][0].length; jj++) {
        if (match[ii][0][jj] == "#" || match[ii][0][jj] == ".") {
          if (match[ii][0][jj] != arr[i + ii][0][j + jj]) {
            c = false;
            break outermatch;
          }
        }
      }
    }
    if (c) {
      ok.push([i, j]);
    }
  }
}
ok.sort((a,b)=>{
    if(a[0]==b[0]){
        return a[1]-b[1]
    }
    else{
        return a[0] - b[0];
    }
})
